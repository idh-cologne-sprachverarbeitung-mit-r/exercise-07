readCorpus <- function(dirname = "data/") {
  vecs <- list()

  for(file in list.files(dirname, pattern="txt$", full.names = TRUE)) {
    lines <- scan(file, what="char", sep="\n", strip.white=TRUE)
    vecs <- append(vecs, list(unlist(strsplit(lines, " "))))
  }

  vecs
}

