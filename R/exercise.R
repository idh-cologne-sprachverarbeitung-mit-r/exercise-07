removeStopwords <- function(vecs) {
  without_stopwords <- list()
  for(v in vecs) {
    # Strip stopwords with %in% (https://discuss.analyticsvidhya.com/t/how-to-remove-value-from-a-vector-in-r/2975)
    without_stopwords <- append(without_stopwords, list(v[!tolower(v) %in% stopwords]))
  }
  without_stopwords
}